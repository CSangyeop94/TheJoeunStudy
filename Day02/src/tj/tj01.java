package tj;

public class tj01 {
    public static void main(String[] args) {

        int su1 = 100;
        int su2 = 50;

        int add = su1 + su2;
        int sub = su1 - su2;
        int gup = su1 * su2;
        int div = su1 / su2;

        System.out.println("덧셈 : " + add);
        System.out.println("뺄셈 : " + sub);
        System.out.println("곱셈 : " + gup);
        System.out.println("나눗셈 : " + div);

    }
}
